def convertir_a_dolar(peso):
    tasa_cambio = 0.0041  # Tasa de cambio de peso argentino a dólar estadounidense
    dolar = peso * tasa_cambio
    return dolar

def convertir_a_euro(peso):
    tasa_cambio = 0.0038  # Tasa de cambio de peso argentino a euro
    euro = peso * tasa_cambio
    return euro

